package com.lyonguyen.esky.logic.models;

public class LevelInfo {

    private double seed;

    private double factor;

    public LevelInfo() {}

    public double getSeed() {
        return seed;
    }

    public void setSeed(double seed) {
        this.seed = seed;
    }

    public double getFactor() {
        return factor;
    }

    public void setFactor(double factor) {
        this.factor = factor;
    }
}
